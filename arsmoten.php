                <h3>Årsmötet 2009<a name="2009"></a></h3>
                <p>
                    Borås spelmanslag stod som värdar för årsmötet den 28 februari, som började kl 16.
                    30-talet medlemmar hade hörsammat inbjudan. Förbundets ordförande Mikael Pettersson öppnade mötet, och efter
                    parentation för de medlemmar under 2008 som lämnat jordelivet överlämnades
                    orförandeklubban till Ingrid Strömvall som åter fick förtroendet att leda årsmötet
                    med Anita Widell som dokumenterade mötet. Den under 2008 arbetande styrelsen beviljades ansvarsfrihet
                    efter tillägg av rapport om 60-årsjubileet i verksamhetsberättelsen.
                    Styrelsen och revisorer fick litet ändrat utseende, Ny ersättare efter Helena
                    Johansson valdes Bengt Johansson, Borås och Dan van Ginhofen ny
                    revisorsersättare efter Ingrid Strömvall. Valberedningen fick förnyat
                    förtroende. Efter årsmötet avnjöts paj med kaffe, och allspel fortsatte in
                    på sena kvällen.
